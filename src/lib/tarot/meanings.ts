// 获取某张牌的完整牌义（自动路由到大阿卡纳手写牌义或小阿卡纳动态合成牌义）
export function getCardMeaning(cardId: string, orientation: 'upright' | 'reversed'): CardMeaning {
  // 1. 判断是否为大阿卡纳
  if (cardId.startsWith('major-')) {
    const numPart = cardId.split('-')[1];
    const num = parseInt(numPart, 10);
    const meaning = majorMeanings[num];
    if (meaning) {
      return meaning[orientation];
    }
  }

  // 2. 小阿卡纳处理
  const parts = cardId.split('-');
  const suit = parts[0];
  const num = parseInt(parts[1], 10);

  if (suitAttributes[suit] && numberAttributes[num]) {
    return generateMinorMeaning(suit, num, orientation);
  }

  // 3. Fallback 安全备用
  return {
    general: `这是一张充满隐喻的塔罗牌，代表您当前所处的某种能量状态。在现实中，它呼应着你关于当下的思考。`,
    love: `在感情关系中，它意味着当前的局势需要你保持专注，多从对方的角度思考，寻求和谐。`,
    career: `在职业工作中，这提示你关注当前执行过程中的细节，明确你的长远规划，稳定发展。`,
    advice: `建议你保持冷静，接纳局势的起伏，相信你内心的直觉选择。`
  };
}
